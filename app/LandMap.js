    render() {
        for (let row = 0; row < NUM_ROWS; row++) {
            for (let col = 0; col < NUM_COLS; col++) {
                let tyleColor = this.grid[row][col] === 1 ? "#222" : "#fff";
                fill(tyleColor);
                stroke("#aaa");
                let tyleX = col * TILE_SIZE;
                let tyleY = row * TILE_SIZE;
                rect(tyleX, tyleY, TILE_SIZE, TILE_SIZE)
            }
        }
    }

    grid() {
        return this.grid;
    }
